package kr.ac.kaist.se.tardis.notification.impl;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;
import javax.persistence.Temporal;
import javax.persistence.TemporalType;

import kr.ac.kaist.se.tardis.notification.api.Notification;

@Entity(name = "notification")
public class NotificationImpl implements Notification {

	@Id
	@GeneratedValue(strategy = GenerationType.SEQUENCE, generator="id_seq")
	@SequenceGenerator(name="id_seq", sequenceName="NOTIFICATION_ID_SEQ")
	private Long id;

	private String username;

	@Column(name = "notificationText")
	private String text;

	@Temporal(TemporalType.DATE)
	private Date notificationDate;

	public NotificationImpl() {
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public Date getNotificationDate() {
		return notificationDate;
	}

	public void setNotificationDate(Date notificationDate) {
		this.notificationDate = notificationDate;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		NotificationImpl other = (NotificationImpl) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}

}
